/// Specifies the associativity of a cache.
export enum EAssociativity
{
	/// Direct mapped cache associativity.
	DirectMapped,

	/// 2-way associativity.
	TwoWay,

	/// 4-way associativity.
	FourWay,

	/// 8-way associativity.
	EightWay,

	/// Fully associative cache associativity.
	FullyAssociative
}

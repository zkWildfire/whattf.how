/// Identifies the various vocabulary sets available to the player.
export const enum EVocabularySet
{
	/// The base set of hiragana characters.
	HiraganaBase,

	/// Hiragana characters with accents.
	HiraganaDakuon,

	/// Multi-character hiragana combinations.
	HiraganaCombo,

	/// The base set of katakana characters.
	KatakanaBase,

	/// Katakana characters with accents.
	KatakanaDakuon,

	/// Multi-character katakana combinations.
	KatakanaCombo
}
